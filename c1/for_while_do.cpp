#include <bits/stdc++.h>
using namespace std;

int main(){
    // FOR
    for(int i=1;i<=5;i++){
        cout<<"Striver Champ"<<i<<endl;
    }

    cout<<endl;

    // WHILE
    int j=1;
    while(j<=5){
        cout<<"Striver Champ"<<j<<endl;
        j++ ;
    }

    cout<<endl;

    // DO WHILE 

    int k=1;
    do{
        cout<<"Striver Champ"<<k<<endl;
        k++ ;
    } while(k<0);

    cout<<k<<endl;

}